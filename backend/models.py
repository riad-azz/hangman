class Word(db.Model):
    id = db.Column(db.Integer, primary_key=True)
    word = db.Column(db.String)

    @staticmethod
    def from_list(words_list: list = None):
        if not words_list:
            words_list = WORDS

        instances = [Word(word=x) for x in words_list]
        db.session.add_all(instances)
        db.session.commit()

    def __str__(self):
        return self.word


db.create_all()

words = Word.query.all()
if not words:
    Word.from_json()
